const { Schema, model } = require("mongoose");

let userInfoSchema = new Schema({
    firstname: {
        type: String,
        required: true,
    },
    lastname: {
        type: String,
        required: true,
    },
    email: {
        type: String,
        required: true,
    },
    address: {
        type: String,
        required: true,
    },
    country: {
        type: String,
        required: true,
    },
    state: {
        type: String,
        required: true,
    },
    zip: {
        type: Number,
        required: true,
    },
    payment: {
        type: Boolean,
        required: true,
    },
});

const UserInfo = model("UserInfo", userInfoSchema);
module.exports = UserInfo;
